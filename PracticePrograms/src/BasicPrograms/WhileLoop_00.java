package BasicPrograms;
/*
 * If the number of iteration is not fixed, then it is recommended to use while loop.
 */
public class WhileLoop_00 {

	public static void main(String[] args) {
		int i = 5;
		while(i <= 10) {
			System.out.print(i+"\t");
			i++;
		}
	}
}
/*
Output:-
5	6	7	8	9	10	
*/